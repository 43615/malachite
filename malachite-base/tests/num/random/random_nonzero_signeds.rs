use malachite_base_test_util::stats::common_values_map::common_values_map;

use malachite_base::num::basic::signeds::PrimitiveSigned;
use malachite_base::num::random::random_nonzero_signeds;
use malachite_base::random::EXAMPLE_SEED;

fn random_nonzero_signeds_helper<T: PrimitiveSigned>(values: &[T], common_values: &[(T, usize)]) {
    let xs = random_nonzero_signeds::<T>(EXAMPLE_SEED);
    assert_eq!(xs.clone().take(20).collect::<Vec<T>>(), values);
    assert_eq!(common_values_map(1_000_000, 10, xs), common_values)
}

